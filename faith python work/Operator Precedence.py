#Operator precedence determines the order with which operators are performed.
##Examples:
     #1.Parentheses:These have the highest precedence.
     #Control flow structures
     #Conditional statenent
     #Loops
     #Control statements
result_one = 3*4+5
print(result_one)

result_two = 3*(4+5)
print(result_two)

result_three = 3*4+5-1
print(result_three)

result_four = 3*(4+5)-1
print(result_four)

